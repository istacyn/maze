/**
 * loadMap - Loads the game map.
 * @map[][MAP_WIDTH]: Array representing game map.
 */
void loadMap(int map[][MAP_WIDTH])
{
	for (int y = 0; y < MAP_HEIGHT; y++)
	{
        	for (int x = 0; x < MAP_WIDTH; x++)
		{
			map[y][x] = gameMap[y][x];
		}
	}
}

/**
 * getMapValue - Function to get a value from the game map.
 * @x: The X-coordinate of the position within the map.
 * @y: The Y-coordinate of the position within the map.
 * Returns: Map value at specified coordinates (x and y) within the map array.
 */
int getMapValue(int map[][MAP_WIDTH], int x, int y)
{
	if (x >= 0 && x < MAP_WIDTH && y >= 0 && y < MAP_HEIGHT)
	{
		return map[y][x];
	}
	return 1; /* Default to a wall tile if outside the map bounds */
}

